package BaiTap4_IfElse;

public class ifElse {
    public static void main(String[] args) {
        int number = 100;
        if (number == 100) {
            System.out.println("Bien number bang 100");
        } else if (number < 50){
            System.out.println("Bien number nho hon 50");
        } else {
            System.out.println("Bien number lon hon 50");
        }
    }
}
